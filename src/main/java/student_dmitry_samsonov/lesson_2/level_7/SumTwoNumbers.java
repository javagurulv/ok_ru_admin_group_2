public class SumTwoNumbers {

    public static void main(String[] args) {

        int numberOne = 1;
        int numberTwo = 2;

        System.out.println("Number 1 = " + numberOne);
        System.out.println("Number 2 = " + numberTwo);

        numberOne = 10;
        numberTwo = 20;

        int sum = numberOne + numberTwo;

        System.out.println("Sum = " + sum);
    }

}