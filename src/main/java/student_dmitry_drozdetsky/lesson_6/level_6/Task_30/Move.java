package student_dmitry_drozdetsky.lesson_6.level_6.Task_30;

public class Move {
    private final int x;
    private final int y;

    Move(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }
}
